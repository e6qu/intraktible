// paritySplitGraph is in -> band(split condition) -> yes:approve / no:review -> out,
// optionally dropping the "no" edge (to fixture the missing-branch failure).
func paritySplitGraph(condition string, withNo bool) events.Graph {
	cfg, _ := json.Marshal(map[string]string{"condition": condition})
	g := events.Graph{
		Nodes: []events.Node{
			parNode("in", events.NodeInput, ""),
			parNode("band", events.NodeSplit, string(cfg)),
			parNode("approve", events.NodeAssignment, `{"assignments":[{"target":"approved","expr":"true"}]}`),
			parNode("review", events.NodeAssignment, `{"assignments":[{"target":"approved","expr":"false"}]}`),
			parNode("out", events.NodeOutput, ""),
		},
		Edges: []events.Edge{
			{From: "in", To: "band"},
			{From: "band", To: "approve", Branch: "yes"},
		},
	}
	if withNo {
		g.Edges = append(g.Edges, events.Edge{From: "band", To: "review", Branch: "no"})
	}
	g.Edges = append(g.Edges, events.Edge{From: "approve", To: "out"}, events.Edge{From: "review", To: "out"})
	return g
}

// buildParityExpect turns a Run into the serialized expectation the vitest replays.
func buildParityExpect(t *testing.T, run domain.Run) parityExpect {
	t.Helper()
	e := parityExpect{Status: string(run.Status), Trace: []parityTraceNode{}}
	for _, r := range run.Results {
		var out any
		if len(r.Output) > 0 {
			if err := json.Unmarshal(r.Output, &out); err != nil {
				t.Fatalf("unmarshal node %q output: %v", r.NodeID, err)
			}
		}
		e.Trace = append(e.Trace, parityTraceNode{NodeID: r.NodeID, Type: string(r.Type), Output: out})
	}
	switch run.Status {
	case domain.StatusCompleted:
		e.Output = run.Output
		if rc, ok := run.Output["reason_codes"].([]any); ok {
			e.ReasonCodes = rc
		}
	case domain.StatusFailed:
		e.FailedNode = run.FailedNode
		e.Error = run.Err
		e.ErrorContains = parityPositionSuffix.ReplaceAllString(run.Err, "")
	case domain.StatusSuspended:
		if run.Suspend == nil {
			t.Fatal("suspended run carries no suspend state")
		}
		e.Suspend = &paritySuspend{
			NodeID:      run.Suspend.NodeID,
			Record:      run.Suspend.Record,
			CompanyName: run.Suspend.Case.CompanyName,
			CaseType:    run.Suspend.Case.CaseType,
			SLADays:     run.Suspend.Case.SLADays,
		}
		if rc, ok := run.Suspend.Record["reason_codes"].([]any); ok {
			e.ReasonCodes = rc
		}
	}
	return e
}

// TestEngineParityFixtures generates the demo-parity battery from the REAL engine
// and asserts the committed fixture file matches what the engine computes today.
// UPDATE_PARITY_FIXTURES=1 rewrites the file; the default run fails loudly when the
// committed fixtures are stale (i.e. engine semantics moved without regenerating).
func TestEngineParityFixtures(t *testing.T) {
	file := parityFile{Readme: parityReadme, Cases: []parityCaseJSON{}}
	seen := map[string]bool{}
	for _, c := range parityCases() {
		if seen[c.name] {
			t.Fatalf("duplicate parity case name %q", c.name)
		}
		seen[c.name] = true
		input := map[string]any{}
		if err := json.Unmarshal([]byte(c.input), &input); err != nil {
			t.Fatalf("case %q input: %v", c.name, err)
		}
		run := domain.Execute(c.graph, input)
		if run.Status != c.want {
			t.Fatalf("case %q: engine returned status %q, the case declares %q (err %q)", c.name, run.Status, c.want, run.Err)
		}
		expect := buildParityExpect(t, run)
		if run.Status == domain.StatusFailed && expect.ErrorContains == "" {
			t.Fatalf("case %q: failed run yields an empty error_contains", c.name)
		}
		file.Cases = append(file.Cases, parityCaseJSON{Name: c.name, Graph: c.graph, Input: input, Expect: expect})
	}

	generated, err := json.MarshalIndent(file, "", "  ")
	if err != nil {
		t.Fatalf("marshal fixtures: %v", err)
	}
	generated = append(generated, '\n')

	path := filepath.FromSlash(parityFixturesPath)
	if os.Getenv("UPDATE_PARITY_FIXTURES") == "1" {
		if err := os.WriteFile(path, generated, 0o644); err != nil {
			t.Fatalf("write fixtures: %v", err)
		}
		t.Logf("wrote %d parity cases to %s", len(file.Cases), path)
		return
	}
	committed, err := os.ReadFile(path)
	if err != nil {
		t.Fatalf("read committed fixtures (%v) — generate them with UPDATE_PARITY_FIXTURES=1 go test ./decision-engine/domain -run TestEngineParityFixtures", err)
	}
	if !bytes.Equal(committed, generated) {
		t.Fatalf("engine-parity fixtures are STALE: %s no longer matches what domain.Execute computes.\nRegenerate with UPDATE_PARITY_FIXTURES=1 go test ./decision-engine/domain -run TestEngineParityFixtures and re-run the web parity vitest.", path)
	}
}
