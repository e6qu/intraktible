func parNode(id string, t events.NodeType, config string) events.Node {
	n := events.Node{ID: id, Type: t}
	if config != "" {
		n.Config = json.RawMessage(config)
	}
	return n
}

// parChain builds in -> nodes... -> out with unconditional edges; the first node
// must be the input and the last the output.
func parChain(nodes ...events.Node) events.Graph {
	g := events.Graph{Nodes: nodes}
	for i := 0; i+1 < len(nodes); i++ {
		g.Edges = append(g.Edges, events.Edge{From: nodes[i].ID, To: nodes[i+1].ID})
	}
	return g
}

// parLine is the common one-worker shape: input -> mid -> output(outCfg).
func parLine(mid events.Node, outCfg string) events.Graph {
	return parChain(parNode("in", events.NodeInput, ""), mid, parNode("out", events.NodeOutput, outCfg))
}

